function initTrackingClicks() {
  document.addEventListener('click', (e) => {
    const hit = e.target.closest('a, button');
    if (!hit) return;
    const track = hit.getAttribute('data-track');
    const href = hit.getAttribute('href') || '';

    if (track) qsTrack(track, { location: 'site' });
    if (href.startsWith('tel:')) qsTrack('phone_click', { href });
    if (href.startsWith('mailto:')) qsTrack('email_click', { href });
  }, { passive: true });
}

/* ─── MOBILE NAV ─── */
function initMobileNav() {
  const nav = document.querySelector('.nav-shell .nav');
  const toggle = document.getElementById('navMobileToggle');
  const icon = toggle ? toggle.querySelector('.nav-mobile-icon') : null;
  const linksWrap = nav ? nav.querySelector('.nav-links') : null;
  if (!nav || !toggle) return;
  toggle.setAttribute('aria-controls', 'primaryNavLinks');
  const getLabel = (open) => T[getCurrentLang()][open ? 'nav_close_menu' : 'nav_open_menu'];

  const closeMenu = () => {
    nav.classList.remove('menu-open');
    toggle.setAttribute('aria-expanded', 'false');
    toggle.setAttribute('aria-label', getLabel(false));
    if (icon) icon.textContent = '☰';
    document.body.classList.remove('nav-lock');
  };
  const toggleMenu = () => {
    const open = nav.classList.toggle('menu-open');
    toggle.setAttribute('aria-expanded', open ? 'true' : 'false');
    toggle.setAttribute('aria-label', getLabel(open));
    if (icon) icon.textContent = open ? '✕' : '☰';
    document.body.classList.toggle('nav-lock', open && window.innerWidth <= 768);
  };

  toggle.setAttribute('aria-expanded', 'false');
  toggle.setAttribute('aria-label', getLabel(false));
  if (icon) icon.textContent = '☰';
  toggle.addEventListener('click', toggleMenu);

  nav.querySelectorAll('.nav-links a').forEach(link => {
    link.addEventListener('click', () => {
      if (window.innerWidth <= 768) closeMenu();
    });
  });

  window.addEventListener('resize', () => {
    if (window.innerWidth > 768) closeMenu();
  });

  document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape') closeMenu();
  });

  document.addEventListener('click', (e) => {
    if (window.innerWidth > 768 || !nav.classList.contains('menu-open')) return;
    if (nav.contains(e.target) || toggle.contains(e.target)) return;
    if (linksWrap && linksWrap.contains(e.target)) return;
    closeMenu();
  });
}

function initActiveNav() {
  const navLinks = Array.from(document.querySelectorAll('.nav-shell .nav-links a[href^="#"]'));
  if (!navLinks.length) return;
  let forcedNavId = null;
  let forcedNavUntil = 0;
  const uniqueTargets = [...new Set(navLinks.map(link => link.getAttribute('href')))];
  const sections = uniqueTargets
    .map(href => document.querySelector(href))
    .filter(Boolean)
    .sort((a, b) => a.offsetTop - b.offsetTop);

  const setActive = (id) => {
    navLinks.forEach(link => {
      const active = link.getAttribute('href') === `#${id}`;
      link.classList.toggle('active', active);
      if (active) link.setAttribute('aria-current', 'page');
      else link.removeAttribute('aria-current');
    });
  };

  const getHeaderOffset = () => {
    const topbar = document.querySelector('.topbar');
    const navShell = document.querySelector('.nav-shell');
    const topbarH = topbar && getComputedStyle(topbar).display !== 'none' ? topbar.offsetHeight : 0;
    const navH = navShell ? navShell.offsetHeight : 0;
    return topbarH + navH + 18;
  };

  const onScroll = () => {
    const offset = getHeaderOffset();
    let current = sections[0]?.id || 'home';

    if (forcedNavId && Date.now() < forcedNavUntil) {
      setActive(forcedNavId);
      return;
    }
    forcedNavId = null;

    for (const section of sections) {
      if (window.scrollY + offset >= section.offsetTop) current = section.id;
      else break;
    }

    const scrolledToBottom = window.innerHeight + window.scrollY >= document.body.scrollHeight - 4;
    if (scrolledToBottom && sections.length) current = sections[sections.length - 1].id;

    setActive(current);
  };

  navLinks.forEach(link => {
    link.addEventListener('click', () => {
      const id = link.getAttribute('href').replace('#', '');
      if (!id) return;
      forcedNavId = id;
      forcedNavUntil = Date.now() + 1300;
      setActive(id);
    });
  });

  window.addEventListener('scroll', onScroll, { passive: true });
  window.addEventListener('resize', onScroll);
  window.addEventListener('hashchange', onScroll);
  onScroll();
}

/* ─── FORM ─── */
function handleSubmit() {
  const form = document.getElementById('contactForm');
  if (!form) return;
  const submitBtn = form.querySelector('button[type="submit"]');
  if (submitBtn) {
    submitBtn.disabled = true;
    submitBtn.setAttribute('aria-busy', 'true');
  }
  const payload = {
    email: form.querySelector('#contactEmail')?.value.trim() || '',
    service: form.querySelector('#contactService')?.value || 'na',
    timeline: form.querySelector('#contactTimeline')?.value || 'na',
    project_type: form.querySelector('#contactProjectType')?.value || 'na'
  };
  const replyTo = document.getElementById('contactReplyTo');
  if (replyTo) replyTo.value = payload.email;
  const next = document.getElementById('contactNext');
  if (next) {
    const u = new URL(window.location.href);
    u.searchParams.set('submitted', '1');
    u.hash = 'contact';
    next.value = u.toString();
  }
  qsTrack('form_submit', {
    service: payload.service || 'na',
    timeline: payload.timeline || 'na',
    project_type: payload.project_type || 'na'
  });
}

